    /**
     * 全関数の逆コンパイル C を全文検索する（nrs_query.py --grep の置き換え）。
     * 初回利用時に in-memory キャッシュを構築し（background thread）、準備完了まで進捗を報告する。
     * 関数名 または 逆コンパイル C に対し case-insensitive でマッチ。番地・名前・一致行を返す。
     */
    private String searchDecompiled(String query, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";
        if (query == null || query.isEmpty()) return "query is required";

        java.util.Map<String,String> cache = decompCache;
        if (cache == null) {
            if (!decompCacheBuilding) startDecompCacheBuild(program);
            return String.format("Decompile cache building: %d/%d functions. Retry shortly.",
                decompCacheDone, decompCacheTotal);
        }

        String ql = query.toLowerCase();
        StringBuilder out = new StringBuilder();
        int found = 0;
        for (java.util.Map.Entry<String,String> e : cache.entrySet()) {
            String c = e.getValue();
            if (c == null) continue;
            String key = e.getKey();                  // "address\tname"
            int tab = key.indexOf('\t');
            String addr = tab >= 0 ? key.substring(0, tab) : key;
            String name = tab >= 0 ? key.substring(tab + 1) : key;
            if (name.toLowerCase().contains(ql) || c.toLowerCase().contains(ql)) {
                out.append(addr).append("  ").append(name).append("\n");
                for (String line : c.split("\n")) {
                    if (line.toLowerCase().contains(ql)) out.append("    > ").append(line.trim()).append("\n");
                }
                out.append("\n");
                if (++found >= limit) { out.append("(limit ").append(limit).append(" reached)\n"); break; }
            }
        }
        return found == 0 ? "No match for '" + query + "'" : out.toString();
    }

    private synchronized void startDecompCacheBuild(Program program) {
        if (decompCacheBuilding || decompCache != null) return;
        decompCacheBuilding = true;
        new Thread(() -> {
            java.util.Map<String,String> cache = new java.util.LinkedHashMap<>();
            DecompInterface decomp = new DecompInterface();
            decomp.openProgram(program);
            ConsoleTaskMonitor monitor = new ConsoleTaskMonitor();
            java.util.List<Function> funcs = new ArrayList<>();
            for (Function f : program.getFunctionManager().getFunctions(true)) funcs.add(f);
            decompCacheTotal = funcs.size();
            for (Function f : funcs) {
                String c = "";
                try {
                    DecompileResults r = decomp.decompileFunction(f, 30, monitor);
                    if (r != null && r.decompileCompleted()) c = r.getDecompiledFunction().getC();
                } catch (Exception ex) { /* leave c empty */ }
                cache.put(f.getEntryPoint().toString() + "\t" + f.getName(), c);
                decompCacheDone++;
            }
            decomp.dispose();
            decompCache = cache;
            decompCacheBuilding = false;
            Msg.info(this, "Decompile cache built: " + cache.size() + " functions.");
        }, "GhidraMCP-DecompCache").start();
    }

    private String getAllClassNames(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        Set<String> classNames = new HashSet<>();
        for (Symbol symbol : program.getSymbolTable().getAllSymbols(true)) {
            Namespace ns = symbol.getParentNamespace();
            if (ns != null && !ns.isGlobal()) {
                classNames.add(ns.getName());
            }
        }
        // ページネーション用に set を list へ変換
        List<String> sorted = new ArrayList<>(classNames);
        Collections.sort(sorted);
        return paginateList(sorted, offset, limit);
    }

    private String listSegments(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        List<String> lines = new ArrayList<>();
        for (MemoryBlock block : program.getMemory().getBlocks()) {
            lines.add(String.format("%s: %s - %s", block.getName(), block.getStart(), block.getEnd()));
        }
        return paginateList(lines, offset, limit);
    }

    private String listImports(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        List<String> lines = new ArrayList<>();
        for (Symbol symbol : program.getSymbolTable().getExternalSymbols()) {
            lines.add(symbol.getName() + " -> " + symbol.getAddress());
        }
        return paginateList(lines, offset, limit);
    }

    private String listExports(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        SymbolTable table = program.getSymbolTable();
        SymbolIterator it = table.getAllSymbols(true);

        List<String> lines = new ArrayList<>();
        while (it.hasNext()) {
            Symbol s = it.next();
            // 古い Ghidra では "export" は isExternalEntryPoint() で判別する
            if (s.isExternalEntryPoint()) {
                lines.add(s.getName() + " -> " + s.getAddress());
            }
        }
        return paginateList(lines, offset, limit);
    }

    private String listNamespaces(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        Set<String> namespaces = new HashSet<>();
        for (Symbol symbol : program.getSymbolTable().getAllSymbols(true)) {
            Namespace ns = symbol.getParentNamespace();
            if (ns != null && !(ns instanceof GlobalNamespace)) {
                namespaces.add(ns.getName());
            }
        }
        List<String> sorted = new ArrayList<>(namespaces);
        Collections.sort(sorted);
        return paginateList(sorted, offset, limit);
    }

    private String listDefinedData(int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";

        List<String> lines = new ArrayList<>();
        for (MemoryBlock block : program.getMemory().getBlocks()) {
            DataIterator it = program.getListing().getDefinedData(block.getStart(), true);
            while (it.hasNext()) {
                Data data = it.next();
                if (block.contains(data.getAddress())) {
                    String label   = data.getLabel() != null ? data.getLabel() : "(unnamed)";
                    String valRepr = data.getDefaultValueRepresentation();
                    lines.add(String.format("%s: %s = %s",
                        data.getAddress(),
                        escapeNonAscii(label),
                        escapeNonAscii(valRepr)
                    ));
                }
            }
        }
        return paginateList(lines, offset, limit);
    }

    private String searchFunctionsByName(String searchTerm, int offset, int limit) {
        Program program = getCurrentProgram();
        if (program == null) return "No program loaded";
        if (searchTerm == null || searchTerm.isEmpty()) return "Search term is required";
    
        List<String> matches = new ArrayList<>();
        for (Function func : program.getFunctionManager().getFunctions(true)) {
            String name = func.getName();
            // 単純な部分文字列マッチ
            if (name.toLowerCase().contains(searchTerm.toLowerCase())) {
                matches.add(String.format("%s @ %s", name, func.getEntryPoint()));
            }
        }
    
        Collections.sort(matches);
    
        if (matches.isEmpty()) {
            return "No functions matching '" + searchTerm + "'";
        }
        return paginateList(matches, offset, limit);
    }    

